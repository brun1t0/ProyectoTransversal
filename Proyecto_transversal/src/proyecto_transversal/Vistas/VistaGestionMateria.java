    private void jBBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBBuscarActionPerformed
      try{
       
          Integer codigo = Integer.parseInt(jTCodigo.getText());
          Materia mat = matData.buscarMateria(codigo);
          jTNombre.setText(mat.getNombre());
          jTAnio.setText(mat.getAnioMateria()+"");
          jRBEstado.setSelected(true);
          
      } catch(NumberFormatException nf){
          JOptionPane.showMessageDialog(this, "CÃ³digo invalido");
      } catch(NullPointerException np){
            JOptionPane.showMessageDialog(this, "Texto vacio"+ np.getMessage());
        }
       
       
        
    }//GEN-LAST:event_jBBuscarActionPerformed

    private void jBGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBGuardarActionPerformed
       // int idMateria = Integer.parseInt(jTCodigo.getText());
      
        try {
            int codigo = Integer.parseInt(jTCodigo.getText());

            if (matData.existeMateria(codigo) == true) {
                modificar();
            } else {
                guardar();
            }
        } catch (NumberFormatException nf) {
            guardar();

        }


    }//GEN-LAST:event_jBGuardarActionPerformed

